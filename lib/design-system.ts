// Type exports for TypeScript support
export type DesignSystem = typeof designSystem
export type ColorScale = keyof typeof designSystem.colors
export type FontSize = keyof typeof designSystem.typography.fontSize
export type Spacing = keyof typeof designSystem.spacing
export type BorderRadius = keyof typeof designSystem.borderRadius
export type Shadow = keyof typeof designSystem.shadows

// Helper functions
export const getColor = (scale: ColorScale, shade = 500) => {
  const colorScale = designSystem.colors[scale]
  if (typeof colorScale === "string") return colorScale
  return (colorScale as any)[shade] || colorScale[500]
}

export const getFontSize = (size: FontSize) => {
  return designSystem.typography.fontSize[size]
}

export const getSpacing = (size: Spacing) => {
  return designSystem.spacing[size]
}

export const getBorderRadius = (size: BorderRadius) => {
  return designSystem.borderRadius[size]
}

export const getShadow = (size: Shadow) => {
  return designSystem.shadows[size]
}
